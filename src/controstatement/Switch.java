package controstatement;

public class Switch {

	public static void main(String[] args) {
		
		int month=10;
		
		 switch(month) {
		 
		 case 1:
			 System.out.println("jan");
		 break;
		 
		 case 9:
			 System.out.println("oct");
		 break;
			 case 8:
				 System.out.println("nov");
				  break;
				 
			 case 12:
				 System.out.println("dec");
		 default:
			 System.out.println("invalid");
		 
		
		
		 }

	}

}
