package practice;

public class Switch {

	public static void main(String[] args) {
	
		int month=10;
		
		switch(month) {
		
		case 1:System.out.println("january");
		break;
		
		case 2:System.out.println("feb");
		break;
		
		case 10:System.out.println("oct");
		break;
		default:System.out.println("dec");
		
		}

	}

}
