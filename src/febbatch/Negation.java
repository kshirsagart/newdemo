package febbatch;

public class Negation {

	public static void main(String[] args) {
		
		boolean b=true;
		
		
		 // ~(negation symbol)
		System.out.println(~5);    // addition   -6
		System.out.println(~-5);   // substaction   4
		 
		System.out.println(~10);  //  -11
		System.out.println(~-10);  // 9
		
		System.out.println(!b);

	}

}
