package febbatch;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int a=20;
		int b=10;
		
		System.out.println(a==20);   //  equal to
		System.out.println(b<10);    // less than
		System.out.println(a>b);     //  greater than
		System.out.println(20>=b);   //  grater than & equal to
System.out.println(10<=b);           //   less than & equal to
System.out.println(a!=b);            //    not equal to  (! is expansion mark)
}
}