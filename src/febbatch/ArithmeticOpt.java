package febbatch;       
public class ArithmeticOpt {    //  java operators

	
public static void main(String[] args) {
		
		int a=20;
		
	    int b=10;
	    
	    // Arithmetic operator
	    System.out.println(a+b);   // addition      
	    System.out.println(a-b);   // substraction
	    System.out.println(a*b);   // multiplication     
	    System.out.println(a/b);   // division
	    System.out.println(a%b );   // percentage
	    
	    // Relational operator  // only for primitive types  except boolean,strings,array
	    
	    System.out.println(a==b);  // equal to
	    System.out.println(a!=b);  //  not equal to   (! expansion mark
	    System.out.println(a>b);   // Greater  than
	    System.out.println(a<b);   // less than
	    System.out.println(a>=b);  // greater than equal to
	    System.out.println(a<=b);  // less than equal to
	    
	    //logical operator  // only for boolean
	    
	    boolean x=true;
	    boolean y=false;
	    
	    System.out.println(x && y);   // and
	    System.out.println(x || y);   //or
	    System.out.println(x | y);    // not
	    
	    // increment operator
	     // a=a+1;
	       a++;
	       System.out.println(a);
	       
	       ++a;
	       System.out.println(a);
	    // decrement operator
		     // b=a-1;
	       b--;
	       System.out.println(b);
	       
	       --b;
	       System.out.println(b);
	       
	     
	       System.out.println(b);
	       
	       // assignment operators
	       
	      int p=40;
	       int q=20;
	       System.out.println(q=p);
	       
	    
	    
	    
	    
		
		
	
	    
	    
	    
	    
	    
	}
}
