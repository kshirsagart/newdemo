package febbatch;

public class Shiftoper {

	public static void main(String[] args) {
		
		// shift operator
		
		// left shift
		System.out.println(10<<4);//10*2^4=10*16=160
		System.out.println(5<<2);  //5*2^2=5*4=20
		
		//right shift
     System.out.println(20>>2);  // 20/2^2=5
     System.out.println(40>>3);  //40/2^3=5
	}

}
